    private void btnComprarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnComprarActionPerformed
            //Carregando as informações digitadas pelo usuário em variaveis
            String nome = txtNome.getText();
            String celular = txtCelular.getText();
            String email = txtEmail.getText();
            String cpf = txtCpf.getText();
            String plano = txtPlano.getText();
            int id = Integer.parseInt(txtPesquisa.getText());
            int quantIngresso = Integer.parseInt(txtIngressos.getText());
            int quantJaTem = Integer.parseInt(txtIngresso.getText());
            String pagamento = "Não Informado";
            
            //Salvando o metodo de paggamento informado pelo usuario na variavel pagamento
            if(pix.isSelected())
                pagamento = "Pix";
            if(cartao.isSelected())
              pagamento = "Cartao";
            if(boleto.isSelected())
              pagamento = "Boleto";
            if(paypal.isSelected())
              pagamento = "Paypal";
            if(mercadopago.isSelected())
              pagamento = "Mercado Pago";
            
            //Comparando para ver see o nome do contato está vazio
            if(txtNome.getText().equals("")){
                //Se estiver exibe uma mensagem de erro para que o usuario informe o id do contato a ser buscado
                JOptionPane.showMessageDialog(null, "Erro ao comprar, você deve procurar um socio por meio de seu ID", "Erro!",0);
            //Comparando de a qauntidade de ingressos for 0 ou menor que 0
            }else if(quantIngresso==0 || quantIngresso<0 || txtIngressos.getText().equals("")){
                //Se for uma das duas opções exibe uma mensagem de erro para seja informada a quantidade de ingressos 
                JOptionPane.showMessageDialog(null, "Erro ao comprar, você deve digitar uma quantidade de ingressos", "Erro!",0); 
            //Compara se o pagamento foi informado
            }else if(pagamento.equals("Não Informado")){
                //Se o pagamento não for informado exibe uma mensagem de erro para que o usuario informe o pagamento
                JOptionPane.showMessageDialog(null, "Erro ao comprar, você deve selecionar uma forma de pagamento", "Erro!",0);
            }else{
               //Se não ocorreu nenhum erro intancia um novo torcedor
               TorcedorController pc = new TorcedorController();
               //E após isso acumula a quantidade de ingressos comprados com o que o torcedor já tem
               quantIngresso += quantJaTem;
                //Chamando o metodo editar para que as alterações do torcedor sejam feitas
                pc.compraIngresso(id,quantIngresso,nome,celular,email,cpf,plano);
                
                //Retorna ao Menu Principal
                this.dispose();
                MenuView menu = new MenuView();
                menu.setVisible(true);
            }
       
    }//GEN-LAST:event_btnComprarActionPerformed

