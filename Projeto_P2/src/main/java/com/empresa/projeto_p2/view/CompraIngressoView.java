    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        //Armazena asinformações dos campos em variveis
        String plano = txtPlano.getText();
        double valorIngresso = 350;
        double valorTotal = 0;
        String ing = txtIngressos.getText();
        
        //Compara para ver se o campo de ingressos está vazio
        if(ing.isEmpty()){
            
        }else{
            //Senão, armazena a quantidade de ingressos em uma variavel
            int ingressos = Integer.parseInt(txtIngressos.getText());
            //Compara se o plano do torcedor é Normal
            if(plano.equals("Normal")){
                //Se for normal, faz as contas com base na quantidade de ingressos comprados
                valorTotal = valorIngresso * ingressos;
                //E seta os valores das conta nos campos para a visualização do usuário
                txtValorUnitario.setText("R$ " + valorIngresso);
                txtValorTotal.setText("R$ " + valorTotal);
            }else{
                //Se não for normal, ele é Gold
                //Então é feito uma conta com 5% de desconto por causa do plano especial
                double desconto = (valorIngresso*5)/100;
                valorIngresso = valorIngresso - desconto;
                valorTotal = valorIngresso * ingressos;
                //Após feitas as contas seta os campos para que o usário veja a contabilidade final
                txtValorUnitario.setText("R$ "+valorIngresso);
                txtValorTotal.setText("R$ " + valorTotal);
            }
        }
    }//GEN-LAST:event_btnCalcularActionPerformed

